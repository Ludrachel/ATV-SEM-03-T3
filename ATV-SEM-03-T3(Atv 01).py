PI = 3.141592
raio = float(input("digite o raio:"))
circunferencia = 2 * PI * raio
a_circulo = PI * raio * 2
a_esfera = 4 * PI * raio ** 2
vol_esfera = 4 / 3 * PI * raio ** 3
print("circunferência: ", circunferencia)
print("Área do círculo: ", a_circulo)
print("Área da esfera ", a_esfera)
print("volume da esfera: ", vol_esfera)
